import { NextResponse, type NextRequest } from "next/server";
import * as z from "zod";
import { prisma } from "@/lib/prisma";
import { verifySession } from "@/lib/auth/dal";
import { SalvarFechamentoSchema, salvarFechamento } from "@/lib/domain/fechamentos";

export async function POST(request: NextRequest) {
  const session = verifySession(request);
  if (!session) {
    return NextResponse.json({ error: "Não autenticado." }, { status: 401 });
  }

  const body = await request.json().catch(() => null);
  const validatedFields = SalvarFechamentoSchema.safeParse(body);
  if (!validatedFields.success) {
    return NextResponse.json(
      { error: z.treeifyError(validatedFields.error) },
      { status: 400 },
    );
  }

  const fechamento = await salvarFechamento(
    prisma,
    session.householdId,
    validatedFields.data,
  );
  return NextResponse.json(fechamento, { status: 201 });
}
