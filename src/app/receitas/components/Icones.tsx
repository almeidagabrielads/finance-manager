export function infoSubtipo(subtipo: string) {
  const valido = SUBTIPOS_RECEITA_UI.some((s) => s.value === subtipo)
    ? (subtipo as SubtipoReceita)
    : "OUTROS";
  return {
    label: labelSubtipoReceita(valido),
    Icone: ICONES_SUBTIPO[valido],
  };
}
